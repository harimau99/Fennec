#include "libft.h"

t_bool		ft_isspace(int c)
{
	if (c < 0 || c > 127)
		return (0);
	if (c == '\t' || c == '\n' || c == '\v' || c == '\f'
		|| c == '\r' || c == ' ')
		return (1);
	else
		return (0);
}

