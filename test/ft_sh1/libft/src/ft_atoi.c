#include "libft.h"

int		ft_atoi(const char *str)
{
	int		result;
	int		neg;

	neg = 0;
	result = 0;
	while (ft_isspace(*str))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			neg = 1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	if (neg)
		result = - result;
	return (result);
}

