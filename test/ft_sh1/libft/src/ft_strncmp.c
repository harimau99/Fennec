#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*s1 && n > 0)
	{
		if (*s2 == '\0')
			return (1);
		if (*s2 > *s1)
			return (-1);
		if (*s1 > *s2)
			return (1);
		s1++;
		s2++;
		n--;
	}
	if (*s2 && n > 0)
		return (-1);
	return (0);
}

